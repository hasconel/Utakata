/**
 * UIコンポーネントの型定義！✨
 * ボタンやアバターなどのUIコンポーネントの型を定義するよ！💖
 */

/**
 * ボタンのサイズ型！✨
 * ボタンの大きさを定義するよ！💖
 */
export type ButtonSize = 'xs' | 'sm' | 'md' | 'lg' | 'xl';

/**
 * ボタンの形状型！✨
 * ボタンの形を定義するよ！💖
 */
export type ButtonShape = 'default' | 'pill' | 'square';

/**
 * アバターのバリアント型！✨
 * アバターのスタイルを定義するよ！💖
 */
export type AvatarVariant = 'default' | 'outline' | 'ghost';

/**
 * バッジの色型！✨
 * バッジの色を定義するよ！💖
 */
export type BadgeColor = 'purple' | 'pink' | 'gray'; 